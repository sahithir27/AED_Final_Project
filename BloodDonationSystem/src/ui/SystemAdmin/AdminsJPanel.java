    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) enterprise_list_tbl.getModel();

        model.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            for (Enterprise enterprise : network.getListOfEnterprises().getEnterpriseList()) {
                for (Users userAccount : enterprise.getListOfUsers().getAccountList()) {
                    Object[] row = new Object[3];
                    row[0] = enterprise.getOrganizationName();
                    row[1] = network.getName();
                    row[2] = userAccount;
                    userstring.add(userAccount.toString());

                    model.addRow(row);
                }
            }
        }
    }

    private void populateNetworkComboBoxDropDown(){
        cbNetwork.removeAllItems();
        
        for (Network network : system.getNetworkList()){
            cbNetwork.addItem(network);
        }
    }
    
    private void populateEnterpriseComboBoxDropDown(Network network){
        cbEnterprise.removeAllItems();
        
        for (Enterprise enterprise : network.getListOfEnterprises().getEnterpriseList()){
            cbEnterprise.addItem(enterprise);
        }
        
    }


    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int selectedRow = enterprise_list_tbl.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "please select an enterprise admin to update");
            return;
        }
        Users user = (Users) enterprise_list_tbl.getValueAt(selectedRow, 2);
        tfUserName.setText(user.getUserName());
        tfPassword.setText(user.getUserPassword());
        tfName.setText(user.getEmployee().getEmployeeName());
        btnSave.setEnabled(true);

    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        int selectedRow = enterprise_list_tbl.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "please select an enterprise admin to update");
            return;
        }
        Users user = (Users) enterprise_list_tbl.getValueAt(selectedRow, 2);
        user.setUserName(tfUserName.getText());
        user.setUserPassword(tfPassword.getText());
        user.getEmployee().setEmployeeName(tfName.getText());
        JOptionPane.showMessageDialog(null, "Details Updated Successfully");
        populateTable();
    }//GEN-LAST:event_btnSaveActionPerformed



    private void cbCurrentEnterpriseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbCurrentEnterpriseActionPerformed
        DefaultTableModel model = (DefaultTableModel) tbl_list_of_enterprises.getModel();
        model.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            if (network.getName() == cbCurrentEnterprise.getSelectedItem()) {
                for (Enterprise enterprise : network.getListOfEnterprises().getEnterpriseList()) {
                    Object[] row = new Object[3];
                    row[0] = enterprise.getOrganizationName();
                    row[1] = network.getName();
                    row[2] = enterprise.getenterpriseType().getType();
                    model.addRow(row);
                }
            }else if(cbCurrentEnterprise.getSelectedIndex()==0){
                loadtable();
            }
        }
    }//GEN-LAST:event_cbCurrentEnterpriseActionPerformed



    private void loadEnterpriseTable() {
        DefaultTableModel entTable = (DefaultTableModel) tbl_list_of_enterprises.getModel();
        entTable.setRowCount(0);
        for (Network network : system.getNetworkList()) {
            for (Enterprise enterprise : network.getListOfEnterprises().getEnterpriseList()) {
                Object[] row = new Object[3];
                row[0] = enterprise.getOrganizationName();
                row[1] = network.getName();
                row[2] = enterprise.getenterpriseType().getType();
                entTable.addRow(row);
            }
        }
    }

    private boolean checkcurrentEnterprise(Enterprise.EnterpriseType type) {
        ArrayList<Enterprise.EnterpriseType> estEnterprises = new ArrayList<Enterprise.EnterpriseType>();
        for (Network network : system.getNetworkList()) {
            if (network == cbCityName.getSelectedItem()) {
                for (Enterprise enterprise : network.getListOfEnterprises().getEnterpriseList()) {
                    estEnterprises.add(enterprise.getenterpriseType());
                }

            }
        }
        if (estEnterprises.contains(type)&&(type.getType().equalsIgnoreCase("Federal Government")||type.getType().equalsIgnoreCase("Blood Bank"))) {
            JOptionPane.showMessageDialog(null, "One enterprise allowed for the selected option");
            return true;
        }

        return false;
    }

    private void LoadComboBox() {
        cbCityName.removeAllItems();
        cbEnterpriseType.removeAllItems();
        cbCityName.addItem("--Select--");
        cbEnterpriseType.addItem("--Select--");
        cbCurrentEnterprise.addItem("-All Enterprises-");
        for (Network network : system.getNetworkList()) {
            cbCityName.addItem(network);
            cbCurrentEnterprise.addItem(network.toString());
        }

        for (Enterprise.EnterpriseType type : Enterprise.EnterpriseType.values()) {
            cbEnterpriseType.addItem(type);
        }

    }
    
    public void loadtable(){
        DefaultTableModel model = (DefaultTableModel) tbl_list_of_enterprises.getModel();
        model.setRowCount(0);
        for (Network network : system.getNetworkList()) {
                for (Enterprise enterprise : network.getListOfEnterprises().getEnterpriseList()) {
                    Object[] row = new Object[3];
                    row[0] = enterprise.getOrganizationName();
                    row[1] = network.getName();
                    row[2] = enterprise.getenterpriseType().getType();
                    model.addRow(row);
                }
            
        }
    }


    private void btnAddNewEnterpriseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddNewEnterpriseActionPerformed

        int counter = 0;
        String name = tfEnterpriseName.getText();
        String city = cbCityName.getSelectedItem().toString();
        if (name.matches("^([A-Za-z]+)(\\s[A-Za-z]+)*\\s?$")) {
            name = name.trim();
            for (String s : entnet) {
                if (s.equalsIgnoreCase(name + city)) {
                    JOptionPane.showMessageDialog(null, "Please try aagain, The Enterprise already exists in this city");
                    counter++;
                    break;
                }
            }
            if (counter == 0) {
                entnet.add(name + city);
                Network network = (Network) cbCityName.getSelectedItem();
                Enterprise.EnterpriseType type = (Enterprise.EnterpriseType) cbEnterpriseType.getSelectedItem();

                if (network == null || type == null) {
                    JOptionPane.showMessageDialog(null, "Please try again, Invalid Input!");
                    return;
                }

                if (checkcurrentEnterprise(type)) {
                    return;

                } else {
                    Enterprise enterprise = network.getListOfEnterprises().createAndAddEnterprise(name, type);

                    loadEnterpriseTable();
                    tfEnterpriseName.setText("");
                }
            }

        } else {
            JOptionPane.showMessageDialog(null, "Invalid input, Enterprise can only contain alphabets");
        }
    }//GEN-LAST:event_btnAddNewEnterpriseActionPerformed


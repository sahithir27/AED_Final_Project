    private void btnCreateNewEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateNewEmployeeActionPerformed
        boolean check;
        check=cbOrganization.getSelectedIndex()>0;
        if(!check){
            JOptionPane.showMessageDialog(null, "No organization is selected, please select an organisation");
            return;
        }

        Organization organization = (Organization) cbOrganization.getSelectedItem();
        String name = tfNameVal.getText();
        for(String s:userList){
            if(s.equals(name)){
                JOptionPane.showMessageDialog(null, "Employee name already exists, please try again");
                return;
            }
        }

        if(name.matches("^([A-Za-z]+)(\\s[A-Za-z]+)*\\s?$")){
            organization.getListOfEmployees().createEmployee(ecoSystem.getEmployeeId(),name);
            ecoSystem.incrementEmployeeId();
            loadInitialEmmployeeTable();
            tfNameVal.setText("");}
        else{
            JOptionPane.showMessageDialog(null, "Try again, names can't contain numbers or special characters");
        }

    }//GEN-LAST:event_btnCreateNewEmployeeActionPerformed


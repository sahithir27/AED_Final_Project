    public void loadOrganizationEmployeeCombobox(){
        cbOrganization.removeAllItems();
        cbOrganization.addItem("--Select--");
        for (Organization organization : organizationDirectory.getOrganizationList()){
            cbOrganization.addItem(organization);
        }
    }

    private void loadInitialEmmployeeTable(){
        DefaultTableModel model = (DefaultTableModel) tblCurrentEmployeeList.getModel();
        
        model.setRowCount(0);

        for(Organization or:organizationDirectory.getOrganizationList()){
            for (Employee employee : or.getListOfEmployees().getListOfEmployees()){
                Object[] row = new Object[2];
                row[0] = employee.getEmployeeId();
                row[1] = employee.getEmployeeName();
                userList.add(employee.getEmployeeName());
                model.addRow(row);
            }
        }
    }

